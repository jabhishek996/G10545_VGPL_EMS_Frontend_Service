const convertNumbers = (obj) => {
  const newObj = {};
  for (let key in obj) {
    const value = obj[key];
    newObj[key] = isNaN(value) ? value : Number(value);
  }
  return newObj;
};

export const getMeterData = async ({ meterId, date }) => {
  const response = await fetch(
    // `http://localhost:3000/api/ems/processeddata/meterdata/at/?meter_id=HT1METER1&date=2026-03-09`
    `http://localhost:3000/api/ems/processeddata/meterdata/at/?meter_id=${meterId}&date=${date}`
  );

  const result = await response.json();
  const data = result.data;

  return {
    status: data.status,
    meter_id: data.meter_name,
    panel_id: data.meter_panel,
    location: data.meter_location,
    date: data.date,

    latest_reading: convertNumbers(data.latest_reading?.[0] || {}),
    avg_reading: convertNumbers(data.avg_reading?.[0] || {}),

    continues_readings: data.continuous_readings
  };
};